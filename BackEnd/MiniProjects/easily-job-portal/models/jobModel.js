let applicants = [];

class Job {
  constructor({
    id,
    jobcategory,
    jobdesignation,
    joblocation,
    companyname,
    salary,
    applyby,
    skillsrequired,
    numberofopenings,
    jobposted,
    applicants,
  }) {
    this.id = id;
    this.jobcategory = jobcategory;
    this.jobdesignation = jobdesignation;
    this.joblocation = joblocation;
    this.companyname = companyname;
    this.salary = salary;
    this.applyby = applyby;
    this.skillsrequired = skillsrequired;
    this.numberofopenings = numberofopenings;
    this.jobposted = jobposted;
    this.applicants = applicants || [];
  }

  addApplicant(applicant) {
    this.applicants.push(applicant);
  }

  static getJobById(id) {
    return jobs.find((job) => job.id === id);
  }

  static getAllJobs() {
    return jobs;
  }

  static createJob(job) {
    const newJob = new Job({ ...job, id: jobs.length + 1, jobposted: new Date() });
    jobs.push(newJob);
    return newJob;
  }

  static updateJob(id, updatedJob) {
    const index = jobs.findIndex((job) => job.id === id);
    if (index !== -1) {
      jobs[index] = { ...jobs[index], ...updatedJob };
      return jobs[index];
    }
    return null;
  }

  static deleteJob(id) {
    const index = jobs.findIndex((job) => job.id === id);
    if (index !== -1) {
      const deletedJob = jobs[index];
      jobs = jobs.filter((job) => job.id !== id);
      return deletedJob;
    }
    return null;
  }

  static getApplicantsByJobId(id) {
    const job = jobs.find((job) => job.id === id);
    return job ? job.applicants : null;
  }

  static addApplicantToJob(id, applicant) {
    const job = jobs.find((job) => job.id === id);
    if (job) {
      job.addApplicant(applicant);
      applicants.push({ ...applicant, jobId: id });
      return job;
    }
    return null;
  }
}

module.exports = Job;

